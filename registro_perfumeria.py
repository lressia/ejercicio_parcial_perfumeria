
class Ventas:

    def __init__(self, num, importe, tipo, apellido, perfume):
        self.num = num
        self.importe = importe
        self.tipo = tipo
        self.apellido = apellido
        self.perfume = perfume

def write(self):
    print(f'Num: {self.num}', end=' ')
    print(f'Importe: {self.importe}', end=' ')
    print(f'Tipo: {self.tipo}', end=' ')
    print(f'Apellido: {self.apellido}', end=' ')
    print(f'Perfume: {self.perfume}', end='\n')

